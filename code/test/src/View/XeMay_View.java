    public void fillToTable() {
        tblModel = (DefaultTableModel) tblXe.getModel();
        tblModel.setRowCount(0);
        ArrayList<SanPham> list = service.getToAll();
        for (SanPham i : list) {
            Object[] rows = new Object[7];
            rows[0] = i.getMaXe();
            rows[1] = i.getTenXe();
            rows[2] = i.getTrangThai()?"Còn Hàng" : "Hết Hàng";
            rows[3] = i.getGiaBan();
            rows[4] = i.getGiaNhap();
            rows[5] = i.getSoLuong();
            tblModel.addRow(rows);
        }
    }

    public void fillToCombo() {
        cboSoLuong.removeAllItems();
        for (int i = 1; i < 3000; i++) {
            cboSoLuong.addItem(i + "");
        }
    }

