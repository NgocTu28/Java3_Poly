    private void tblXeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblXeMouseClicked
        int index = tblXe.getSelectedRow();
        if (index >= 0) {
            txtMa1.setText(tblXe.getValueAt(index, 0).toString());
            txtTen.setText(tblXe.getValueAt(index, 1).toString());
            txtGiaBan.setText(tblXe.getValueAt(index, 3).toString());
            txtGiaNhap.setText(tblXe.getValueAt(index, 4).toString());
            if (tblXe.getValueAt(index, 2).equals("Còn Hàng")) {
                cboTrangThai.setSelectedIndex(0);
            } else if (tblXe.getValueAt(index, 2).equals("Hết Hàng")) {
                cboTrangThai.setSelectedIndex(1);
            }
            cboSoLuong.setSelectedIndex((int) tblXe.getValueAt(index, 5)-1);

        }
    }//GEN-LAST:event_tblXeMouseClicked

    public SanPham getSP() {
//        if (txtMa1.getText().trim().isEmpty() || txtTen.getText().trim().isEmpty()) {
//            JOptionPane.showMessageDialog(this, "Không được để trống.");
//            return null;
//        }

        int giaBan, giaNhap;
        try {
            giaBan = Integer.parseInt(txtGiaBan.getText());
            giaNhap = Integer.parseInt(txtGiaNhap.getText());

            if (giaBan < 0 || giaNhap < 0) {
                JOptionPane.showMessageDialog(this, "Giá tiền phải lớn hơn 0.");
                return null;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Giá tiền phải là số nguyên.");
            return null;
        }

        SanPham sp = new SanPham();
        sp.setMaXe(txtMa1.getText());
        sp.setTenXe(txtTen.getText());
        sp.setGiaBan(giaBan);
        sp.setGiaNhap(giaNhap);
        if(cboTrangThai.getSelectedIndex() == 0){
            sp.setTrangThai(true);
        }else {
            sp.setTrangThai(false);
        }
        sp.setSoLuong(cboSoLuong.getSelectedIndex() + 1);
        return sp;
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SanPham sp = getSP();
        if(sp != null){
            service.add(sp);
            fillToTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

