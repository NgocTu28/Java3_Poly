    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int index = tblSinhVien.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Chọn Dòng.");
            return;
        }
        tblModel.setValueAt(txtTen.getText().trim(), index, 1);
        tblModel.setValueAt(cboDVT.getSelectedItem(), index, 2);
        tblModel.setValueAt(txtDonGia.getText().trim(), index, 3);
        tblModel.setValueAt(txtNhaCungCap.getText().trim(), index, 4);
    }//GEN-LAST:event_jButton2ActionPerformed

