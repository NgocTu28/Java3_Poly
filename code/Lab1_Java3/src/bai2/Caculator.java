    public Boolean checkNull (){
        if(txtFirst.getText().isEmpty()){
            JOptionPane.showConfirmDialog(this,"Bạn chưa nhập giá trị.","Thông báo", JOptionPane.ERROR_MESSAGE);
            txtFirst.requestFocus();
            return false;
        }else  if(txtSecond.getText().isEmpty()){
            JOptionPane.showConfirmDialog(this,"Bạn chưa nhập giá trị.","Thông báo", JOptionPane.ERROR_MESSAGE);
            txtSecond.requestFocus();
            return false;
        }else {
            return true;
        }
    }
   
